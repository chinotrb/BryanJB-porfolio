  updatePageMeta() {
    // Update page title and meta description based on language
    const titles = {
      es: 'Bryan J. Brenes - Portfolio | Frontend Dev ü•äüéÆ',
      en: 'Bryan J. Brenes - Portfolio | Frontend Dev ü•äüéÆ'
    };
    
    const descriptions = {
      es: 'Portfolio de Bryan J. Brenes - Desarrollador Frontend, Pixel Artist y Boxing Enthusiast',
      en: 'Bryan J. Brenes Portfolio - Frontend Developer, Pixel Artist and Boxing Enthusiast'
    };
    
    document.title = titles[this.currentLang];
    
    const metaDescription = document.querySelector('meta[name="description"]');
    if (metaDescription) {
      metaDescription.setAttribute('content', descriptions[this.currentLang]);
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = this.currentLang;
  }
  
  updateActiveButton() {
    const langButtons = document.querySelectorAll('.lang-btn');
    langButtons.forEach(btn => {
      btn.classList.remove('active');
      if (btn.getAttribute('data-lang') === this.currentLang) {
        btn.classList.add('active');
      }
    });
  }
  
